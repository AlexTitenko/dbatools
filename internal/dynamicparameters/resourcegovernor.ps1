Register-ArgumentCompleter -ParameterName ResourcePools -ScriptBlock {
	param (
		$commandName,
		$parameterName,
		$wordToComplete,
		$commandAst,
		$fakeBoundParameter
	)
	
	$server = Get-SmoServerForDynamicParams
	$collection = $server.ResourceGovernor.ResourcePools | Where-Object { $_.Name -notin "internal", "default" } | Select -Exp Name
	
	if ($collection)
	{
		foreach ($item in $collection)
		{
			New-CompletionResult -CompletionText $item -ToolTip $item
		}
	}
}